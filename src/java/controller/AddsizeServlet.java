    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String r_id = request.getParameter("pid");
        String name = request.getParameter("name");
        String r_q = request.getParameter("quantity");
        int id, q;
        try {
            id = Integer.parseInt(r_id);
            q = Integer.parseInt(r_q);
            DAO_E d = new DAO_E();
            d.addQuantityProduct(name, q, id);
            request.setAttribute("pid", id);

        } catch (Exception e) {
        }
        request.getRequestDispatcher("newproduct.jsp").forward(request, response);
    }

