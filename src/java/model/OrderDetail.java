package model;

/**
 *
 * @author ADMIN
 */
public class OrderDetail {
    private int pid;
    private int oid;
    private int quantity;
    private double price;
    private String size;

    public OrderDetail() {
    }

    public OrderDetail(int pid, int oid, int quantity, double price, String size) {
        this.pid = pid;
        this.oid = oid;
        this.quantity = quantity;
        this.price = price;
        this.size = size;
    }

    public int getPid() {
        return pid;
    }

    public void setPid(int pid) {
        this.pid = pid;
    }

    public int getOid() {
        return oid;
    }

    public void setOid(int oid) {
        this.oid = oid;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }
    
    
}
